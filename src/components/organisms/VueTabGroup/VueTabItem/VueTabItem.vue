<template>
  <transition @beforeEnter="beforeEnter" @enter="enter" @beforeLeave="beforeLeave" @leave="leave">
    <section v-if="show" :class="cssClasses" role="tabpanel">
      <slot />
    </section>
  </transition>
</template>

<script lang="ts">
import anime from 'animejs';

export default {
  name: 'VueTabItem',
  inject: ['register', 'updateHeader'],
  props: {
    title: { type: String, required: true },
    isActive: { type: Boolean, default: false },
  },
  data(): any {
    return {
      idx: null,
      active: false,
    };
  },
  computed: {
    show() {
      return this.active;
    },
    cssClasses(): any {
      const classes = [this.$style.vueTab];

      if (this.active) {
        classes.push(this.$style.active);
      }

      return classes;
    },
  },
  watch: {
    title(title: string) {
      this.updateHeader(this.idx, { title });
    },
  },
  created() {
    this.active = this.isActive;

    this.register(this);
  },
  methods: {
    beforeEnter(el: HTMLElement) {
      el.style.opacity = '0.2';
    },
    enter(el: HTMLElement, done: any) {
      done();
      anime({
        targets: el,
        opacity: {
          value: '1',
          duration: 1000,
          elasticity: 0,
        },
        easing: 'easeInOutSine',
        complete: done,
      });
    },
    beforeLeave(el: HTMLElement) {
      el.style.transform = '0.8';
    },
    leave(el: HTMLElement, done: any) {
      done();
      anime({
        targets: el,
        opacity: {
          value: '0',
          duration: 700,
          elasticity: 0,
        },
        easing: 'easeInOutSine',
        complete: done,
      });
    },
  },
};
</script>

<style lang="scss" module>
@import '~@/assets/design-system';

.vueTab {
  transition: $tab-item-transition;
}
</style>
